function [] = neurUI(varargin)
 % open existing figure
    openfig('IdentifyNrnsFig.fig');
    hObject = gcf;
    handles = hObject.Children;
    axes = {handles(3),handles(2),handles(1)};


%sort all the variables...   
    
 
    labels = varargin{1,1};
    scaling = varargin{1,2};
    ellipseData = varargin{1,3};
    
    profileObject = varargin{1,4};
    imName = varargin{1,5};
    
 % plot all the neurons into the    
 [searchData,lightHandle,handleFrame,handleDat] = displayNeuronSpheres(labels,scaling,ellipseData{1},profileObject,axes);
    S.searchData = searchData;
    S.lightHandle = lightHandle;
    S.handleFrame = handleFrame;
    S.handleDat = handleDat;
    S.axes = axes;
    S.profileObject = profileObject;
    S.scaling = scaling; 
    
    S.nextStep = 1;
    fileID = fopen('allNeurons.txt');
    C = textscan(fileID,'%s %s');
    fclose(fileID);
    S.theList = C{1};
    S.cycling = 1;
    S.proceed = 0;
    S.data = [];
    S.str = '';
    S.geomData = ellipseData{1};
    S.ellipseData = ellipseData;
    S.outputData = horzcat(searchData,labels,labels);
    S.nameNeuron = 1;
    S.itsDone = false;
    S.h = '';
    S.xpos = 0;
    S.ypos = 0;
    S.zpos = 0;
    S.lx = 10;
    S.ly = 10;
    S.lz = 10;
    S.angleX = 0;
    S.angleY = 0;
    S.angleZ = 0;
    S.toggleV = false;
    S.hSlice = '';
    stack_green  = getImageMiji( [cd,'\',imName],[],[1],[1] );
    S.stack_green = permute(stack_green,[2,1,3]);
    stack_red  = getImageMiji( [cd,'\',imName],[],[2],[1] );
    S.stack_red = permute(stack_red,[2,1,3]);
    S.stack = stack_green;
    S.stackColor = 1;
    %S.ax1 = gca;
    S.xRange = axes{1}.XLim;
    S.yRange = axes{1}.YLim;
    S.zRange = axes{1}.ZLim;
    S.line = [];
    S.changeGeom = 0;
    S.clicked = 0;
    S.processing = 0;
    S.firstSwitch = 1; 
    S.colorRange = [1 4095];
    S.lowerDRLim = 1;
    S.upperDRLim = 4095;
    S.addANeuron = false;
    S.dataColorMap = 'jet';
    S.dataAx = nan;
    S.deleteANrn = false;
    S.positionLog = [0,0,0];
    S.c = 1;
    assignin('caller','S',S);

    uiwait(hObject);




% UIWAIT makes IdentifyNrnsFig wait for user response (see UIRESUME)
% uiwait(handles.figure1);
end


